package polynomual;

/**
 *
 * @author Aslan
 */
public class Nomual {

    int pow;
    double ceoff;

    public Nomual(int pow, double ceoff) {
        this.pow = pow;
        this.ceoff = ceoff;
    }

    public int getPow() {
        return this.pow;
    }

    public void setPow(int pow) {
        this.pow = pow;
    }

    public double getCeoff() {
        return this.ceoff;
    }

    public void setCeoff(double ceoff) {
        this.ceoff = ceoff;
    }
}
